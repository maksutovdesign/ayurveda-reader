# ── sthana mapping ─────────────────────────────────────────────────────────

NEW_STHANAS = [
    'Чарака — Сутрастхана',
    'Чарака — Шарирастхана',
    'Чарака — Нидана стхана',
    'Чарака — Вимана стхана',
    'Чарака — Индрия стхана',
    'Чарака — Чикитса стхана',
    'Чарака — Калпастхана',
    'Чарака — Сиддхистхана',
]

# Which existing Charaka chapter numbers belong to which new sthana
ACTIVE_REMAP = {
    1:  'Чарака — Сутрастхана',
    9:  'Чарака — Сутрастхана',
    25: 'Чарака — Сутрастхана',
    30: 'Чарака — Сутрастхана',
    5:  'Чарака — Шарирастхана',
}

STUB_DATA = {
    'Чарака — Сутрастхана':    SUTRA,
    'Чарака — Шарирастхана':   SHARIRA,
    'Чарака — Нидана стхана':  NIDANA,
    'Чарака — Вимана стхана':  VIMANA,
    'Чарака — Индрия стхана':  INDRIYA,
    'Чарака — Чикитса стхана': CHIKITSA,
    'Чарака — Калпастхана':    KALPA,
    'Чарака — Сиддхистхана':   SIDDHI,
}

# ── main ───────────────────────────────────────────────────────────────────

def main():
    with open(INPUT, 'r', encoding='utf-8') as f:
        raw = f.read()
    assert raw.startswith(PREFIX) and raw.endswith(SUFFIX)
    data = json.loads(raw[len(PREFIX):-len(SUFFIX)])

    with open(BACKUP, 'w', encoding='utf-8') as f:
        f.write(raw)
    print(f'Backed up → {BACKUP}')

    # ── 1. Update sthanas array ────────────────────────────────────────────
    sthanas = data['sthanas']
    # Remove old "Чарака-самхита", insert new ones before "Випассана"
    sthanas = [s for s in sthanas if s != 'Чарака-самхита']
    vip_idx = sthanas.index('Випассана')
    for s in reversed(NEW_STHANAS):
        sthanas.insert(vip_idx, s)
    data['sthanas'] = sthanas
    print(f'Sthanas: {sthanas}')

    # ── 2. Remap existing Charaka chapters ────────────────────────────────
    for ch in data['chapters']:
        if ch.get('sthana') == 'Чарака-самхита':
            new_sthana = ACTIVE_REMAP.get(ch['number'])
            if new_sthana:
                ch['sthana'] = new_sthana
                print(f'  Remapped Ch#{ch["number"]} → {new_sthana}')
            else:
                print(f'  WARNING: Ch#{ch["number"]} has no remap — left as-is')

    # ── 3. Collect existing chapter numbers per new sthana ────────────────
    existing = {}
    for ch in data['chapters']:
        s = ch.get('sthana', '')
        existing.setdefault(s, set()).add(ch['number'])

    # ── 4. Add stubs ──────────────────────────────────────────────────────
    added = 0
    new_chapters = []
    for sthana, chapters_list in STUB_DATA.items():
        present = existing.get(sthana, set())
        for num, title, subtitle in chapters_list:
            if num not in present:
                new_chapters.append({
                    'number':    num,
                    'title':     title,
                    'subtitle':  subtitle,
                    'sthana':    sthana,
                    'available': False,
                    'content':   [],
                })
                added += 1

    data['chapters'].extend(new_chapters)

    # ── 5. Sort chapters within each sthana by number ────────────────────
    by_sthana = {}
    others = []
    for ch in data['chapters']:
        s = ch.get('sthana', '')
        if s in data['sthanas']:
            by_sthana.setdefault(s, []).append(ch)
        else:
            others.append(ch)

    ordered = others[:]
    for s in data['sthanas']:
        group = sorted(by_sthana.get(s, []), key=lambda c: c['number'])
        ordered.extend(group)

    data['chapters'] = ordered

    print(f'\nAdded {added} stubs. Total chapters: {len(ordered)}')

    with open(INPUT, 'w', encoding='utf-8') as f:
        f.write(PREFIX + json.dumps(data, ensure_ascii=False) + SUFFIX)
    print('Written.')

if __name__ == '__main__':
    main()
